function [R2 R3 R5 R7 R8 S0 S1 S2]=Anglecal(R1,R4,R6)
M=zeros(3,3);
M(1,:)=1/2*[1 cos(2*0*pi/180) sin(2*0*pi/180)];
M(2,:)=1/2*[1 cos(2*60*pi/180) sin(2*60*pi/180)];
M(3,:)=1/2*[1 cos(2*120*pi/180) sin(2*120*pi/180)];

M=M^(-1);

S0=M(1,1)*R1+M(1,2)*R4+M(1,3)*R6;
S1=M(2,1)*R1+M(2,2)*R4+M(2,3)*R6;
S2=M(3,1)*R1+M(3,2)*R4+M(3,3)*R6;

R2=1/2*(S0+S1*cos(2*30*pi/180)+S2*sin(2*30*pi/180));
R3=1/2*(S0+S1*cos(2*45*pi/180)+S2*sin(2*45*pi/180));
R5=1/2*(S0+S1*cos(2*90*pi/180)+S2*sin(2*90*pi/180));
R7=1/2*(S0+S1*cos(2*135*pi/180)+S2*sin(2*135*pi/180));
