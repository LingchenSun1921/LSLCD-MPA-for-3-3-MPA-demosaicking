function [R2 R4 R6 R8 S0 S1 S2]=SSCalculate(a,b,c,d)
M=zeros(4,3);
M(1,:)=1/2*[1 cos(2*0*pi/180) sin(2*0*pi/180)];
M(2,:)=1/2*[1 cos(2*45*pi/180) sin(2*45*pi/180)];
M(3,:)=1/2*[1 cos(2*90*pi/180) sin(2*90*pi/180)];
M(4,:)=1/2*[1 cos(2*135*pi/180) sin(2*135*pi/180)];
M=pinv(M);
S0=M(1,1)*a+M(1,2)*b+M(1,3)*c+M(1,4)*d;
S1=M(2,1)*a+M(2,2)*b+M(2,3)*c+M(2,4)*d;
S2=M(3,1)*a+M(3,2)*b+M(3,3)*c+M(3,4)*d;

% R1 = 1/2*S0 + 1/2*cos(2*0*pi/180)*S1 + 1/2* sin(2*0*pi/180)*S2;
R2 = 1/2*S0 + 1/2*cos(2*30*pi/180)*S1 + 1/2* sin(2*30*pi/180)*S2;
% R3 = 1/2*S0 + 1/2*cos(2*45*pi/180)*S1 + 1/2* sin(2*45*pi/180)*S2;
R4 = 1/2*S0 + 1/2*cos(2*60*pi/180)*S1 + 1/2* sin(2*60*pi/180)*S2;
% R5 = 1/2*S0 + 1/2*cos(2*90*pi/180)*S1 + 1/2* sin(2*90*pi/180)*S2;
R6 = 1/2*S0 + 1/2*cos(2*120*pi/180)*S1 + 1/2* sin(2*120*pi/180)*S2;
% R7 = 1/2*S0 + 1/2*cos(2*135*pi/180)*S1 + 1/2* sin(2*135*pi/180)*S2;
R8 = 1/2*S0 + 1/2*cos(2*150*pi/180)*S1 + 1/2* sin(2*150*pi/180)*S2;
end
